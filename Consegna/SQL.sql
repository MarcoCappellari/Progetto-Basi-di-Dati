create table Cliente (
    Mail            varchar (50)    primary key,
    Nome            varchar (50)    not null,
    Cognome         varchar (50)    not null,
    DataNascita     date            not null,
    NumeroTelefono  varchar(20)     not null     unique
);


create table Tessera (
    Cliente          varchar(50)   primary key, 
    DataIscrizione   date          not null,
    Punti            int           default 0     check (Punti >=0),
    foreign key (Cliente)    references Cliente(Mail)    on update cascade on delete cascade
);


create table Cameriere (
   IdCameriere     int           primary key,
   DataAssunzione  date          not null,
   Nome            varchar(20)   not null,
   Cognome         varchar(20)   not null,
   DataNascita     date          not null,
   Sesso           char(1)       not null     CHECK (Sesso IN ('M', 'F')),
   Telefono        varchar (20)  not null     unique,
   PagaOraria      decimal (4,2) not null     CHECK(PagaOraria>0),
   CHECK (DataNascita < DataAssunzione AND AGE(DataAssunzione, DataNascita) >= INTERVAL '18 years')
	
);


create table Cuoco (
   IdCuoco          int           primary key,
   DataAssunzione   date          not null,
   Nome             varchar(20)   not null,
   Cognome          varchar(20)   not null,
   DataNascita      date          not null,
   Sesso            char(1)       not null      CHECK (Sesso IN ('M', 'F')),
   Telefono         varchar(20)   not null      unique,
   PagaPerPizza     decimal (3,2) not null      CHECK(PagaPerPizza>0),
   PizzePerOra      int           not null      CHECK(PizzePerOra>0),
   CHECK (DataNascita < DataAssunzione AND AGE(DataAssunzione, DataNascita) >= INTERVAL '18 years')
);



create table Tavolo (
    NumeroTavolo    int            primary key,
    Posti           int            not null  check (posti>0),
    Sala            varchar (15)   not null, 
    Cameriere       int,
    foreign key (Cameriere)    references Cameriere(IdCameriere) on update set null on delete set null
);

create table Prenota (
    Ora           time,
    DataP          date,
    Mail_Cliente  varchar(50) not null,
    NumeroTavolo  int,
    primary key (Ora, DataP, NumeroTavolo),
    foreign key (Mail_Cliente) references Cliente(Mail)        on update cascade on delete cascade,
    foreign key (NumeroTavolo) references Tavolo(NumeroTavolo) on update cascade on delete cascade
);

create table Ingrediente (
    NomeIngrediente      varchar(20)  primary key,
    Surgelato            bool         not null,
    ValoreNutrizionale   int          not null      check (ValoreNutrizionale >=0),
    Disponibile          bool         not null
   
);

create table Pizza (
   NomePizza     varchar(20)   primary key,
   Vegetariana   bool          not null,
   Prezzo        decimal(5,2)  not null
);


create table Contiene (
   NomePizza        varchar(20),
   NomeIngrediente  varchar(20),
   primary key (NomePizza, NomeIngrediente),
   foreign key (NomePizza)       references Pizza(NomePizza)             on update cascade on delete cascade,
   foreign key (NomeIngrediente) references Ingrediente(NomeIngrediente) on update cascade on delete cascade
);



create table Ordinazione (
   IdOrdinazione    int         primary key,
   OraOrdinazione   time        not null,
   DataOrdinazione  date        not null,
   Mail_Cliente     varchar(50) not null,
   Cuoco            int         not null,
   foreign key (Cuoco)           references Cuoco(IdCuoco)    on update cascade on delete cascade,
   foreign key (Mail_Cliente)    references Cliente(Mail)     on update cascade on delete cascade
   
);



create table Appartiene (
   NomePizza       varchar(20),
   IdOrdinazione   int,
   quantita        int           not null    check (quantita >0),
   primary key (NomePizza, IdOrdinazione),   
   foreign key (NomePizza)       references Pizza(NomePizza)              on update cascade on delete cascade,
   foreign key (IdOrdinazione)   references Ordinazione(IdOrdinazione)    on update cascade on delete cascade
);


create index IndiceOrdinazione on Ordinazione (DataOrdinazione);


